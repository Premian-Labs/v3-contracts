import Dotenv from 'dotenv';
import { AggregatorV3Interface__factory } from '../typechain';
import { BigNumber } from 'ethers';
Dotenv.config();

function getProvider() {
  return new AlchemyProvider('homestead', process.env.API_KEY_ALCHEMY);
}

async function main() {
  const provider = getProvider();

  for (const el of feeds) {
    const feed = AggregatorV3Interface__factory.connect(el, provider);

    const data = await feed.latestRoundData();
    let phaseId = data.roundId.shr(64);
    // const aggregatorRoundId = data.roundId.and(
    //   BigNumber.from('0xffffffffffffffff'),
    // );
    // console.log(data.roundId, phaseId.shl(64).or(aggregatorRoundId));

    console.log('---');
    console.log(el);

    let aggregatorRoundId = 1;
    while (phaseId.toNumber() > 0) {
      try {
        const data = await feed.getRoundData(
          phaseId.shl(64).or(aggregatorRoundId),
        );
      } catch (e) {
        console.log(e);
        console.log(`Error round ${aggregatorRoundId} (Phase ${phaseId})`);

        if (aggregatorRoundId == 20) {
          phaseId = phaseId.sub(1);
          aggregatorRoundId = 1;
        }

        aggregatorRoundId++;
        continue;
      }

      console.log(data.answer.toString(), data.updatedAt.toString());

      phaseId = phaseId.sub(1);
      aggregatorRoundId = 1;
    }
  }
}

main().catch(console.error);
